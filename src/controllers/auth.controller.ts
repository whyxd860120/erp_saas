/**
 * 用户注册（创建租户 + 管理员账号）
 * POST /api/v1/auth/register
 */
export const register = async (req: Request, res: Response) => {
  try {
    // 兼容前后端参数：支持 email 和 username
    const { username, email, name, password, tenantName, tenantSlug } = req.body;
    const emailOrUsername = email || username; // 优先使用 email，否则使用 username

    if (!emailOrUsername || !name || !password || !tenantName || !tenantSlug) {
      return res.status(400).json({
        success: false,
        message: '参数不完整',
      });
    }

    // 检查用户名是否已存在（系统用户）
    const existingSystemUser = await prisma.systemUser.findUnique({
      where: { email: emailOrUsername },
    });

    if (existingSystemUser) {
      return res.status(400).json({
        success: false,
        message: '用户名已被注册',
      });
    }

    // 检查用户是否已经创建过租户（限制只能试用一次）
    const existingUserWithTenant = await prisma.user.findFirst({
      where: { email: emailOrUsername },
    });

    if (existingUserWithTenant) {
      return res.status(400).json({
        success: false,
        message: '该用户名已注册过租户，如需继续使用请联系管理员',
      });
    }

    // 检查租户名称是否已存在
    const existingTenantByName = await prisma.tenant.findFirst({
      where: { name: tenantName },
    });

    if (existingTenantByName) {
      return res.status(400).json({
        success: false,
        message: '机构名称已存在',
      });
    }

    // 检查租户标识是否已存在（支持中文slug）
    // 如果输入的是纯中文，自动转换为拼音slug
    let finalSlug = tenantSlug;
    
    // 如果slug是纯中文，转换为拼音
    if (/^[\u4e00-\u9fa5]+$/.test(tenantSlug)) {
      // 使用拼音转换
      finalSlug = convertToPinyin(tenantSlug);
    }
    
    const existingTenantBySlug = await prisma.tenant.findUnique({
      where: { slug: finalSlug },
    });

    if (existingTenantBySlug) {
      return res.status(400).json({
        success: false,
        message: '机构标识已存在',
      });
    }

    // 加密密码
    const hashedPassword = await bcrypt.hash(password, 10);

    // 创建租户和管理员用户（事务）
    const result = await prisma.$transaction(async (tx) => {
      // 创建租户
      const tenant = await tx.tenant.create({
        data: {
          name: tenantName,
          slug: finalSlug, // 使用处理后的slug
          plan: config.tenant.defaultPlan,
          status: 'trial',
          trialEndsAt: new Date(Date.now() + config.tenant.defaultTrialDays * 24 * 60 * 60 * 1000),
        },
      });

      // 创建管理员用户（租户内用户）
      const user = await tx.user.create({
        data: {
          tenantId: tenant.id,
          email: emailOrUsername,
          username: emailOrUsername, // 使用邮箱作为用户名
          password: hashedPassword,
          name,
          code: generateUserCode(tenant.id), // 生成人员编码
          role: 'admin',
          status: 'active',
          isSystemUser: true, // 管理员是系统用户，可以登录
        },
      });

      return { tenant, user };
    });

    // 生成token
    const token = generateToken({
      id: result.user.id,
      email: result.user.email,
      role: result.user.role,
      tenantId: result.tenant.id,
    });

    // 记录审计日志
    await auditLog({
      tenantId: result.tenant.id,
      userId: result.user.id, // 确保这是 User 表的 ID
      action: 'create',
      module: 'tenant',
      resource: result.tenant.id,
      detail: JSON.stringify({ tenantName, tenantSlug: finalSlug, email: emailOrUsername, name }),
      ip: req.ip,
      userAgent: req.get('user-agent'),
    });

    return res.status(201).json({
      success: true,
      data: {
        token,
        tenant: {
          id: result.tenant.id,
          name: result.tenant.name,
          slug: result.tenant.slug,
          plan: result.tenant.plan,
          status: result.tenant.status,
          trialEndsAt: result.tenant.trialEndsAt,
        },
        user: {
          id: result.user.id,
          email: result.user.email,
          name: result.user.name,
          role: result.user.role,
        },
      },
      message: '注册成功',
    });
  } catch (error) {
    console.error('注册错误:', error);
    return res.status(500).json({
      success: false,
      message: '注册失败',
    });
  }
};

