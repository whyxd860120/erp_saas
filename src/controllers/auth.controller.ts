/**
 * 根据用户名搜索可用的租户列表
 * GET /api/v1/auth/search-tenants
 */
export const searchTenantsByUsername = async (req: Request, res: Response) => {
  try {
    const { username } = req.query;

    if (!username || typeof username !== 'string') {
      return res.json({
        success: true,
        data: { tenants: [], count: 0 },
      });
    }

    const tenants: any[] = [];

    // 查询系统用户
    try {
      const systemUsers = await prisma.systemUser.findMany({
        where: {
          OR: [
            { email: username },
            { name: username },
          ],
        },
        include: { tenant: true },
      });

      for (const user of systemUsers) {
        if (user.tenant) {
          tenants.push({
            id: user.tenant.id,
            name: user.tenant.name,
            slug: user.tenant.slug,
            isSystem: user.tenant.isSystem || false,
          });
        }
      }
    } catch (e) {
      console.error('查询系统用户失败:', e);
    }

    // 查询租户用户
    try {
      const tenantUsers = await prisma.user.findMany({
        where: {
          OR: [
            { email: username },
            { name: username },
          ],
        },
        include: { tenant: true },
      });

      for (const user of tenantUsers) {
        if (user.tenant && !tenants.find(t => t.id === user.tenant!.id)) {
          tenants.push({
            id: user.tenant.id,
            name: user.tenant.name,
            slug: user.tenant.slug,
          });
        }
      }
    } catch (e) {
      console.error('查询租户用户失败:', e);
    }

    // 去重
    const uniqueTenants = tenants.filter((t, index, arr) => 
      arr.findIndex(x => x.id === t.id) === index
    );

    return res.json({
      success: true,
      data: { tenants: uniqueTenants, count: uniqueTenants.length },
    });
  } catch (error) {
    console.error('搜索租户错误:', error);
    return res.json({
      success: true,
      data: { tenants: [], count: 0 },
    });
  }
};

